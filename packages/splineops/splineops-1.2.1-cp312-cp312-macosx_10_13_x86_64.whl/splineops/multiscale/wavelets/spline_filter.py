    def _compute_g(self):
        """
        Follows the Java code's approach:
          if (order > 0)
             g[0] = h[0]
             for k=1..len(h)-1:
                if k is even => g[k] =  h[k]
                else         => g[k] = -h[k]
          else
             g[0] = -h[0]
        """
        if self.h is None:
            raise ValueError("self.h is not set.")
        n = len(self.h)
        self.g = np.zeros(n, dtype=float)

        if self.order > 0:
            self.g[0] = self.h[0]
            for k in range(1, n):
                if (k % 2) == 0:   # even
                    self.g[k] = self.h[k]
                else:             # odd
                    self.g[k] = -self.h[k]
        else:
            self.g[0] = -self.h[0]
        # done
